    def forward(self, im_rgb, im_t, augment=False, visualize=False):
        # YOLOv5 MultiBackend inference
        b, ch, h, w = im_t.shape  # batch, channel, height, width
        if self.fp16 and im_t.dtype != torch.float16 and im_rgb.dtype != torch.float16:
            im_rgb = im_rgb.half()  # to FP16
            im_t = im_t.half()  # to FP16
        if self.nhwc:
            im_rgb = im_rgb.permute(0, 2, 3, 1)  # torch BCHW to numpy BHWC shape(1,320,192,3)
            im_t = im_t.permute(0, 2, 3, 1)  # torch BCHW to numpy BHWC shape(1,320,192,3)

        if self.pt:  # PyTorch
            y = self.model(im_rgb, im_t)
        elif self.engine:  # TensorRT
            if self.dynamic and (im_rgb.shape != self.bindings["input0"].shape or im_t.shape != self.bindings["input1"].shape):
                i = self.model.get_binding_index("input0")
                self.context.set_binding_shape(i, im_rgb.shape)  # reshape if dynamic
                i = self.model.get_binding_index("input1")
                self.context.set_binding_shape(i, im_t.shape)  # reshape if dynamic
                self.bindings["input0"] = self.bindings["input0"]._replace(shape=im_rgb.shape)
                self.bindings["input1"] = self.bindings["input1"]._replace(shape=im_t.shape)
                for name in self.output_names:
                    i = self.model.get_binding_index(name)
                    self.bindings[name].data.resize_(tuple(self.context.get_binding_shape(i)))
            s = self.bindings["input0"].shape
            assert im_rgb.shape == s, f"input size {im_rgb.shape} {'>' if self.dynamic else 'not equal to'} max model size {s}"
            self.binding_addrs["input0"] = int(im_rgb.data_ptr())
            self.binding_addrs["input1"] = int(im_t.data_ptr())
            self.context.execute_v2(list(self.binding_addrs.values()))
            y = [self.bindings[x].data for x in sorted(self.output_names)]

        if isinstance(y, (list, tuple)):
            return self.from_numpy(y[0]) if len(y) == 1 else [self.from_numpy(x) for x in y]
        else:
            return self.from_numpy(y)

    def from_numpy(self, x):
        return torch.from_numpy(x).to(self.device) if isinstance(x, np.ndarray) else x

    def warmup(self, imgsz=(1, 3, 640, 640)):
        # Warmup model by running inference once
        warmup_types = self.pt, self.jit, self.onnx, self.engine, self.saved_model, self.pb, self.triton
        if any(warmup_types) and (self.device.type != 'cpu' or self.triton):
            im_rgb = torch.empty(*imgsz, dtype=torch.half if self.fp16 else torch.float, device=self.device)  # input
            im_t = torch.empty(*imgsz, dtype=torch.half if self.fp16 else torch.float, device=self.device)  # input            
            for _ in range(2 if self.jit else 1):  #
                self.forward(im_rgb, im_t)  # warmup

    @staticmethod
    def _model_type(p='path/to/model.pt'):
        # Return model type from model path, i.e. path='path/to/model.onnx' -> type=onnx
        # types = [pt, jit, onnx, xml, engine, coreml, saved_model, pb, tflite, edgetpu, tfjs, paddle]
        sf = ['pt']
        types = [s in Path(p).name for s in sf]
        #  types[8] &= not types[9]  # tflite &= not edgetpu
        # triton = not any(types) and all([any(s in url.scheme for s in ['http', 'grpc']), url.netloc])
        return types # + [triton]

    @staticmethod
    def _load_metadata(f=Path('path/to/meta.yaml')):
        # Load metadata from meta.yaml if it exists
        if f.exists():
            d = yaml_load(f)
            return d['stride'], d['names']  # assign stride, names
        return None, None

