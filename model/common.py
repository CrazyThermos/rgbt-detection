class RGBTDetectMultiBackend(nn.Module):
    # YOLOv5 MultiBackend class for python inference on various backends
    def __init__(self, backendmodel, weights='yolov5s.pt', device=torch.device('cpu'), dnn=False, data=None, fp16=False, fuse=True):
        # Usage:
        #   PyTorch:              weights = *.pt
        #   TorchScript:                    *.torchscript
        #   ONNX Runtime:                   *.onnx
        #   ONNX OpenCV DNN:                *.onnx --dnn
        #   OpenVINO:                       *_openvino_model
        #   CoreML:                         *.mlmodel
        #   TensorRT:                       *.engine
        #   TensorFlow SavedModel:          *_saved_model
        #   TensorFlow GraphDef:            *.pb
        #   TensorFlow Lite:                *.tflite
        #   TensorFlow Edge TPU:            *_edgetpu.tflite
        #   PaddlePaddle:                   *_paddle_model
        

        super().__init__()
        w = str(weights[0] if isinstance(weights, list) else weights)
        
        pt = self._model_type(w)[0]
        jit = False
        engine = weights[0].split('.')[-1] == 'engine'
        onnx = False
        saved_model = False
        pb = False
        triton = False
        nhwc = False
        fp16 &= pt or jit or onnx or engine or triton  # FP16
        # BHWC formats (vs torch BCWH)
        stride = 32  # default stride
        cuda = torch.cuda.is_available() and device.type != 'cpu'  # use CUDA
        # if not (pt or triton):
        #     w = attempt_download(w)  # download if not local

        if pt:  # PyTorch
            # weights = torch.load(weights)
            if isinstance(weights, list):
                weights = weights[0]
            ckpt = torch.load(weights, map_location='cpu') 
            csd = ckpt['model'].float().state_dict()
            csd = intersect_dicts(csd, backendmodel.state_dict(), exclude=[])
            backendmodel.load_state_dict(csd, strict=False)
            stride = max(int(backendmodel.detect_block.stride.max()), 32)  # backendmodel stride
            names = backendmodel.module.names if hasattr(backendmodel, 'module') else backendmodel.names  # get class names
            backendmodel.half() if fp16 else backendmodel.float()
            self.model = backendmodel  # explicitly assign for to(), cpu(), cuda(), half()
        elif engine:
            LOGGER.info(f"Loading {w} for TensorRT inference...")
            import tensorrt as trt  # https://developer.nvidia.com/nvidia-tensorrt-download

            check_version(trt.__version__, "7.0.0", hard=True)  # require tensorrt>=7.0.0
            if device.type == "cpu":
                device = torch.device("cuda:0")
            Binding = namedtuple("Binding", ("name", "dtype", "shape", "data", "ptr"))
            logger = trt.Logger(trt.Logger.INFO)
            with open(w, "rb") as f, trt.Runtime(logger) as runtime:
                model = runtime.deserialize_cuda_engine(f.read())
            context = model.create_execution_context()
            bindings = OrderedDict()
            output_names = []
            fp16 = False  # default updated below
            dynamic = False
            for i in range(model.num_bindings):
                name = model.get_binding_name(i)
                dtype = trt.nptype(model.get_binding_dtype(i))
                if model.binding_is_input(i):
                    if -1 in tuple(model.get_binding_shape(i)):  # dynamic
                        dynamic = True
                        context.set_binding_shape(i, tuple(model.get_profile_shape(0, i)[2]))
                    if dtype == np.float16:
                        fp16 = True
                else:  # output
                    output_names.append(name)
                shape = tuple(context.get_binding_shape(i))
                im = torch.from_numpy(np.empty(shape, dtype=dtype)).to(device)
                bindings[name] = Binding(name, dtype, shape, im, int(im.data_ptr()))
            binding_addrs = OrderedDict((n, d.ptr) for n, d in bindings.items())
            batch_size = bindings["input0"].shape[0]
        else:
            raise NotImplementedError(f'ERROR: {w} is not a supported format')

        # class names
        if 'names' not in locals():
            names = yaml_load(data)['names'] if data else {i: f'class{i}' for i in range(999)}
        # if names[0] == 'n01440764' and len(names) == 1000:  # ImageNet
        #     names = yaml_load(ROOT / 'data/ImageNet.yaml')['names']  # human-readable names

        self.__dict__.update(locals())  # assign all variables to self

    def forward(self, im_rgb, im_t, augment=False, visualize=False):
        # YOLOv5 MultiBackend inference
        b, ch, h, w = im_t.shape  # batch, channel, height, width
        if self.fp16 and im_t.dtype != torch.float16 and im_rgb.dtype != torch.float16:
            im_rgb = im_rgb.half()  # to FP16
            im_t = im_t.half()  # to FP16
        if self.nhwc:
            im_rgb = im_rgb.permute(0, 2, 3, 1)  # torch BCHW to numpy BHWC shape(1,320,192,3)
            im_t = im_t.permute(0, 2, 3, 1)  # torch BCHW to numpy BHWC shape(1,320,192,3)

        if self.pt:  # PyTorch
            y = self.model(im_rgb, im_t)
        elif self.engine:  # TensorRT
            if self.dynamic and (im_rgb.shape != self.bindings["input0"].shape or im_t.shape != self.bindings["input1"].shape):
                i = self.model.get_binding_index("input0")
                self.context.set_binding_shape(i, im_rgb.shape)  # reshape if dynamic
                i = self.model.get_binding_index("input1")
                self.context.set_binding_shape(i, im_t.shape)  # reshape if dynamic
                self.bindings["input0"] = self.bindings["input0"]._replace(shape=im_rgb.shape)
                self.bindings["input1"] = self.bindings["input1"]._replace(shape=im_t.shape)
                for name in self.output_names:
                    i = self.model.get_binding_index(name)
                    self.bindings[name].data.resize_(tuple(self.context.get_binding_shape(i)))
            s = self.bindings["input0"].shape
            assert im_rgb.shape == s, f"input size {im_rgb.shape} {'>' if self.dynamic else 'not equal to'} max model size {s}"
            self.binding_addrs["input0"] = int(im_rgb.data_ptr())
            self.binding_addrs["input1"] = int(im_t.data_ptr())
            self.context.execute_v2(list(self.binding_addrs.values()))
            y = [self.bindings[x].data for x in sorted(self.output_names)]

        if isinstance(y, (list, tuple)):
            return self.from_numpy(y[0]) if len(y) == 1 else [self.from_numpy(x) for x in y]
        else:
            return self.from_numpy(y)

    def from_numpy(self, x):
        return torch.from_numpy(x).to(self.device) if isinstance(x, np.ndarray) else x

    def warmup(self, imgsz=(1, 3, 640, 640)):
        # Warmup model by running inference once
        warmup_types = self.pt, self.jit, self.onnx, self.engine, self.saved_model, self.pb, self.triton
        if any(warmup_types) and (self.device.type != 'cpu' or self.triton):
            im_rgb = torch.empty(*imgsz, dtype=torch.half if self.fp16 else torch.float, device=self.device)  # input
            im_t = torch.empty(*imgsz, dtype=torch.half if self.fp16 else torch.float, device=self.device)  # input            
            for _ in range(2 if self.jit else 1):  #
                self.forward(im_rgb, im_t)  # warmup

    @staticmethod
    def _model_type(p='path/to/model.pt'):
        # Return model type from model path, i.e. path='path/to/model.onnx' -> type=onnx
        # types = [pt, jit, onnx, xml, engine, coreml, saved_model, pb, tflite, edgetpu, tfjs, paddle]
        sf = ['pt']
        types = [s in Path(p).name for s in sf]
        #  types[8] &= not types[9]  # tflite &= not edgetpu
        # triton = not any(types) and all([any(s in url.scheme for s in ['http', 'grpc']), url.netloc])
        return types # + [triton]

    @staticmethod
    def _load_metadata(f=Path('path/to/meta.yaml')):
        # Load metadata from meta.yaml if it exists
        if f.exists():
            d = yaml_load(f)
            return d['stride'], d['names']  # assign stride, names
        return None, None

